SELECT HOTELES.ID, HOTELES.NOMBRE, 
PISCINA.ID AS PISCINA,
PISCINA.PROFUNDIDAD AS PISCINA_PROFUNDIDAD,
PISCINA.COSTO AS PISCINA_COSTO,
PISCINA.CAPACIDAD AS PISCINA_CAPACIDAD,
GIMNASIO.ID AS GIMNASIO, 
GIMNASIO.MAQUINAS AS GIMNASIO_MAQUINAS, 
GIMNASIO.HORARIO AS GIMNASIO_HORARIO, 
GIMNASIO.CAPACIDAD AS GIMNASIO_CAPACIDAD, 
INTERNET.ID AS INTERNET, 
INTERNET.CAPACIDAD AS INTERNET_CAPACIDAD,
BARES.ID AS BAR,
BARES.NOMBRE AS BAR_NOMBRE,
RESTAURANTES.ID AS RESTAURANTE,
RESTAURANTES.NOMBRE AS RESTAURANTE_NOMBRE,
SUPERMERCADOS.ID AS SUPERMERCADO,
SUPERMERCADOS.NOMBRE AS SUPERMERCADO_NOMBRE,
TIENDAS.ID AS TIENDA,
TIENDAS.NOMBRE AS TIENDA_NOMBRE

FROM HOTELES
JOIN PISCINA ON HOTELES.ID = PISCINA.HOTELID
JOIN GIMNASIO ON HOTELES.ID = GIMNASIO.HOTELID
JOIN INTERNET ON HOTELES.ID = INTERNET.HOTELID
JOIN BARES ON HOTELES.ID = BARES.HOTELID
JOIN RESTAURANTES ON HOTELES.ID = RESTAURANTES.HOTELID
JOIN SUPERMERCADOS ON HOTELES.ID = SUPERMERCADOS.HOTELID
JOIN TIENDAS ON HOTELES.ID = TIENDAS.HOTELID

GROUP BY HOTELES.ID, HOTELES.NOMBRE, PISCINA.ID, PISCINA.PROFUNDIDAD, PISCINA.COSTO, PISCINA.CAPACIDAD, GIMNASIO.ID, GIMNASIO.MAQUINAS, GIMNASIO.HORARIO, GIMNASIO.CAPACIDAD,
INTERNET.ID, INTERNET.CAPACIDAD, BARES.ID, BARES.NOMBRE, RESTAURANTES.ID, RESTAURANTES.NOMBRE, SUPERMERCADOS.ID, SUPERMERCADOS.NOMBRE, TIENDAS.ID, TIENDAS.NOMBRE;
















